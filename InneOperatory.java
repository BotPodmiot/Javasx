public class InneOperatory {
    public static void main(String[] args) {
    int number = 10;
    int otherNumber = 3;
    number += 5;
    System.out.println(number);
    
    number -= 3;
    System.out.println(number);

    number += otherNumber;
    System.out.println(number);

    number *= 2;
    System.out.println(number); 

    System.out.println(number -= 7);

    System.out.println(number = 100);
    }
}
